// primitive types : number , string , boolean
// complex types : arrays, object
// Function type : parameters

// primitive types

let myNum: number;

myNum = 10.3;

let myString: string;

myString = "hello";

let myBool: boolean;

myBool = false;

// Complex Types

let myStrArr: string[];

myStrArr = ["name", "hell"];

let myPersonObj: {
  name: string;
  age: number;
};

myPersonObj = {
  name: "Gaurav",
  age: 4,
};

let myObjectArr: {
  name: string;
  age: number;
}[];

myObjectArr = [
  {
    name: "Gaurav",
    age: 4,
  },
];
